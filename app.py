# Calculadora simple

def sumar(a, b):
    """Función que recibe dos números y devuelve su suma"""
    return a + b

def restar(a, b):
    """Función que recibe dos números y devuelve su resta"""
    return a - b

# Funcion Nueva
def multiplicar(a, b):
    return a * b

